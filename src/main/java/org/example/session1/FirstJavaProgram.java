package org.example.session1;

public class FirstJavaProgram {
    public static void main(String[] args) {
        System.out.println("This is my first program in java");
    } //End of main
} //End of First Java Program

// public: phương thức có thể được gọi từ bất kỳ nơi nào.
// static: phương thức thuộc về lớp, không cần tạo đối tượng để chạy.
// void: phương thức không trả về giá trị.
// main: tên phương thức đặc biệt mà JVM tìm để chạy chương trình.
// String[] args: tham số truyền vào chương trình (nếu chạy bằng dòng lệnh).
//System.out.println() là lệnh in ra màn hình console.

