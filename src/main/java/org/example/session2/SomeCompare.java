package org.example.session2;

public class SomeCompare {
    public void someCompare(){
        // Compare integer
        System.out.println(100 == 100);
        // Compare char
        System.out.println('a' == 'a');
        // Compare char & float
        System.out.println('a' == 97.1f);
        // Compare boolean
        System.out.println(true == true);
    }
}
